    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
         if (jTableCliente.getSelectedRow() >= 0) {
            int op = JOptionPane.showConfirmDialog(null, "Deseja realmente realizar esta operação?", "Atenção", JOptionPane.YES_NO_OPTION);
                if(op==0){
                    this.cliente = clientes.get(jTableCliente.getSelectedRow());
                    controle.removerCiente(cliente);
                    listarTodosClientes();
                     JOptionPane.showConfirmDialog(null, "O Cliente foi Removido!", "Atenção", JOptionPane.YES_NO_OPTION);
                    preencherTabela();
                }
            
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um CLIENTE na tabela para excluir!!!", "Atenção", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

