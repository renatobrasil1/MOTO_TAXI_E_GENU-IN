    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        if(jTableCliente.getSelectedRow() >= 0){
            this.cliente = clientes.get(jTableCliente.getSelectedRow());
            TelaCliente tela = new TelaCliente(null,true);        
             tela.setCliente(cliente);
             tela.setVisible(true);
            listarTodosClientes();
            preencherTabela();
        }else {
             JOptionPane.showMessageDialog(null, "Selecione um CLIENTE na tabela para alterar !!!", "Atenção", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btnAlterarActionPerformed

